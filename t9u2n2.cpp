#include <iostream> // Подключаем библиотеку для работы с вводом и выводом
#include <fstream>  // Подключаем библиотеку для работы с файлами
#include <string>   // Подключаем библиотеку для работы со строками

using namespace std; // Используем стандартное пространство имен

int main() { // Начало главной функции
    ifstream in("f.txt"); // Открываем файл f для чтения
    ofstream outNeg("g.txt"); // Открываем файл g для записи отрицательных чисел
    ofstream outPos("h.txt"); // Открываем файл h для записи положительных чисел

    if (!in) { // Проверяем, открылся ли файл f
        cout << "Error opening file f"; // Если нет, выводим сообщение об ошибке
        return 1; // Возвращаем ненулевой код ошибки
    }

    int number; // Объявляем переменную для хранения считанного числа

    while (in >> number) { // Читаем числа из файла f
        if (number < 0) { // Если число отрицательное
            outNeg << number << endl; // Записываем его в файл g
        }
        else { // Если число положительное
            outPos << number << endl; // Записываем его в файл h
        }
    }

    in.close(); // Закрываем файл f
    outNeg.close(); // Закрываем файл g
    outPos.close(); // Закрываем файл h

    return 0; // Возвращаем 0, завершая программу успешно
}